// Generates src/assets/build-stamp.js — a tiny script the DEMO loads to show a
// git# build stamp + the live version triple (ionicons/Stencil/component),
// mirroring aedh's About build stamp. DEMO-ONLY: it lives under src/assets
// (copied to www/) and is never imported by the published component.
//
// Run automatically via the `prebuild` / `prestart` npm hooks.
import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const pkgVersion = (p) => JSON.parse(readFileSync(join(root, p), 'utf8')).version

const git = (cmd, fallback) => {
  try {
    return execSync(`git ${cmd}`, { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] })
      .toString().trim()
  } catch {
    return fallback // CI shallow clone / git absent — degrade gracefully
  }
}

const ionicons = pkgVersion('node_modules/ionicons/package.json')
const stencil = pkgVersion('node_modules/@stencil/core/package.json')
const component = pkgVersion('package.json')

const stamp = {
  triple: `${ionicons}/${stencil}/${component}`, // ionicons / Stencil / component
  git: git('rev-parse --short HEAD', 'unknown'),
  branch: git('rev-parse --abbrev-ref HEAD', 'unknown'),
  built: new Date().toISOString()
}

const out = join(root, 'src/assets')
mkdirSync(out, { recursive: true })
writeFileSync(
  join(out, 'build-stamp.js'),
  `/* AUTO-GENERATED by scripts/gen-build-stamp.mjs — do not edit. Demo-only. */\n` +
  `window.AE_BUILD_STAMP = ${JSON.stringify(stamp)};\n`
)
console.log('[build-stamp]', stamp.triple, stamp.git, stamp.branch, stamp.built)
